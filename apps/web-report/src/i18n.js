export function translate(language, key, ...args) {
  const lang = MESSAGES[language] ? language : "en";
  const value = key
    .split(".")
    .reduce((current, segment) => (current && segment in current ? current[segment] : undefined), MESSAGES[lang]);

  if (typeof value === "function") {
    return value(...args);
  }

  return value ?? key;
}

export function localizeText(language, zh, en) {
  return language === "zh-CN" ? zh : en;
}
